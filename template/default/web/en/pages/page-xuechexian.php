<script type="text/javascript">
    //用于存放标签对象以及调用基础函数
    var rollContent={
        initialize:function(){
            this.rollbox=$(".baop-content-n3 .content");
            this.rollbody=$("tbody",this.rollbox);
            this.rolllist=$("tr",this.rollbody);
            this.ANIMATE_TIME=500;
            this.initEvent();//为标签绑定事件函数
        },
        initEvent:function(){
            if(this.rolllist.length>10){
                this.rollbody.append(this.rolllist.slice(0,10).clone());
                this.setAnimate();
            }
        },
        setAnimate:function(){
            var that=this,table=this.rollbox.children();
            table.stop(true,true).animate({
                "margin-top":-this.rolllist.length*38
            },this.ANIMATE_TIME*this.rolllist.length,'linear',function(){
                table.css("margin-top",0);
                that.setAnimate();
            })

        }
    }

    $(function(){
        rollContent.initialize();
    })
</script>
